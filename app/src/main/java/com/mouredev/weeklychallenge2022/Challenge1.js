const verificarAnagrama = (palabra1,palabra2)=>{
    if(palabra1.toLowerCase() == palabra2.toLowerCase()){
        return false;
    }
    let pal1 = palabra1.toLowerCase().split('').sort();
    let pal2 = palabra2.toLowerCase().split('').sort();
    for(pal in pal2){
        if(pal1[pal] == (pal2[pal])){
            return true;
        }else{
            return false;
        }
    }
}
